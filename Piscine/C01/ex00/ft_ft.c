#include <unistd.h>

void	ft_ft(int *nbr)
{
	char	u;
	char	d;

	*nbr = 42;
	u = (*nbr / 10) + '0';
	d = (*nbr % 10) + '0';
	write(1, &u, 1);
	write(1, &d, 1);
}

int	main(void)
{
	int	a;

	a = 34;
	ft_ft(&a);
	return (0);
}
